// O concat() serve para concatenar duas ou mais strings em uma nova string. Ele não modifica as strings originais, mas cria uma nova string
// contendo a concatenação das strings originais.

let primeiroNome = "Anderson";
let segundoNome = " Santos";
let resultado = primeiroNome.concat(segundoNome);
console.log(resultado);

